public class Day3 {
    public static void main(String[] args){
        //Control Statements
        //IF ELSE-IF
        int x = 5;
        int y = 10;

        if( x > y)
            System.out.println(x + " is greater than" + y);
        else if (x == y)
            System.out.println(x + " is equal to " + y);
        else
            System.out.println(x + " is less than " + y);

    }
}
